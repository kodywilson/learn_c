#include <stdio.h>

int main() {
  int a = 0;
  int b = 0;

  // print the values

  printf("a is %d\n", a);
  printf("b is %d\n", b);

  // show differences between ++n and n++

  printf("a++ is %d\n", a++);
  printf("++b is %d\n", ++b);

  // print values again

  printf("a is %d\n", a);
  printf("b is %d\n", b);

  return 0;
}
